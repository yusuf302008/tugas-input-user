public class Buku {
    int id;
    String judul;
    String pengarang;
    int tahunTerbit;

    //contructor parameter
    public Buku(int id, String judul, String pengarang, int tahunTerbit){
        //variabel = parameter
        this.id = id;
        this.judul = judul;
        this.pengarang = pengarang;
        this.tahunTerbit = tahunTerbit;


    }  
}
